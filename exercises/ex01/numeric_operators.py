"""Numeric Operators: This demonstration will help me see how numbers work."""

__author__ = "730401590"

value_1 = str(input("Left-hand side: "))
value_1 = int(value_1)
value_2 = str(input("Right-hand side: "))
value_2 = int(value_2)

print(value_1, "**", value_2, "is", value_1 ** value_2)
print(value_1, "/", value_2, "is", value_1 / value_2)
print(value_1, "//", value_2, "is", value_1 // value_2)
print(value_1, "%", value_2, "is", value_1 % value_2)
